#include<iostream>
using namespace std;
int main(){
    
     // ----USING FOR LOOP---
    for (int i = 0; i < 5; i++)
    {
       cout<<"HELLO WORLD"<<endl;
    }
  cout<<" "<<endl;
  
    // --- USING WHILE LOOP ---
    int i=0;
    while (i<5)
       {
           cout<<"WELCOME"<<endl;
           i++;
       }
      
    
    
    return 0;
}